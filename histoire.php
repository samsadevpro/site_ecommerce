        <h1>Notre Histoire</h1>
        <p>Je m'appelle <strong>Dalil</strong>, et avec mon meilleur pote <strong>Sami</strong>, on est en BTS SIO, plongés dans le code et les bases de données toute la journée. Mais notre vrai kiff, c'est le streetwear, et surtout les sneakers. On passe notre temps à scruter les dernières sorties, à chercher les meilleures paires, à analyser les tendances. Et un jour, on s'est dit : pourquoi ne pas transformer cette passion en business ?</p>
        <p>Tout a commencé autour d'un café après les cours. Sami et moi, on parlait des reventes de sneakers, du marché qui explose, des marques qui cartonnent. On savait coder, on connaissait le web, alors l'idée a germé : créer notre propre site de vente de sneakers, une plateforme où les passionnés comme nous pourraient acheter et revendre leurs paires en toute confiance.</p>
        <p>Le plus dur, c'était de se lancer. On a commencé par coder un premier prototype en utilisant nos connaissances en développement web. Entre deux projets de BTS, on bossait sur le site, testant des fonctionnalités, cherchant un design qui nous ressemblait. Mais il nous manquait un truc essentiel : du stock.</p>
        <p>On a alors décidé d'investir nos économies dans quelques paires rares. On a chassé les éditions limitées, écumé les groupes de revente, flairé les bonnes affaires. Petit à petit, notre stock a grandi et notre site aussi. On a bossé les fiches produits, peaufiné l'expérience utilisateur, intégré un module de paiement sécurisé.</p>
        <p>Quand on a enfin lancé le site, on a eu peur. Et si personne ne venait ? Et si on s'était plantés ? Mais grâce aux réseaux sociaux, aux forums spécialisés, au bouche-à-oreille, les commandes ont commencé à tomber. Les premières ventes nous ont donné une claque : ça marchait !</p>
        <p>Aujourd'hui, on jongle entre les cours et notre projet, entre les lignes de code et les colis à expédier. Ce n'est que le début, mais on rêve déjà plus grand. Pourquoi pas ouvrir une boutique physique un jour ? Ou collaborer avec des designers pour créer nos propres modèles ?</p>
        <p>Ce qui est sûr, c'est qu'on n'a jamais été aussi motivés. On n'a pas juste créé un site, on a construit un rêve. Et on compte bien le faire grandir.</p>

